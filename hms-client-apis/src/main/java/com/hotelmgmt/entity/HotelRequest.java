package com.hotelmgmt.entity;

/**
 * The Class HotelRequest.
 * 
 * @author Gokulan
 */
public class HotelRequest {

    /**
     * The name.
     */
    public String name;

    /**
     * The city.
     */
    public String city;

    /**
     * The state.
     */
    public String state;

    /**
     * Gets the name.
     *
     * @return the name
     */
    public String getName() {
	return name;
    }

    /**
     * Sets the name.
     *
     * @param name the new name
     */
    public void setName(String name) {
	this.name = name;
    }

    /**
     * Gets the city.
     *
     * @return the city
     */
    public String getCity() {
	return city;
    }

    /**
     * Sets the city.
     *
     * @param city the new city
     */
    public void setCity(String city) {
	this.city = city;
    }

    /**
     * Gets the state.
     *
     * @return the state
     */
    public String getState() {
	return state;
    }

    /**
     * Sets the state.
     *
     * @param state the new state
     */
    public void setState(String state) {
	this.state = state;
    }
}
